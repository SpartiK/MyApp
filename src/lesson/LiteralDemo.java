package lesson;

public class LiteralDemo {
    public static void main(String[] args) {
        boolean a = false;
        int b = 255;
        String c = "Привет";
        char e = 'a';
        int f = 0b111;
        int g = 020;
        int h = 878;
        int q = 0x1115;
        float k = .412f;
        double s = .412;

        System.out.println("Логический литерал " + a);
        System.out.println("Десятичный литерал типа int " + b);
        System.out.println("строковый литерал " + c);
        System.out.println("символьный литерал " + e);
        System.out.println("целочисленный 2-й " + f);
        System.out.println("целочисленный 8-й " + g);
        System.out.println("целочисленный 10-й " + h);
        System.out.println("целочисленный 16-й " + q);
        System.out.println("литерал типа float " + k);
        System.out.println("итерал типа double " + s);

    }


}
